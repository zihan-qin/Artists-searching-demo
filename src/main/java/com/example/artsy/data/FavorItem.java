package com.example.artsy.data;

public class FavorItem {
    public String name;
    public String id;
    public String birthday;
    public String nationality;

    public FavorItem(String name, String id, String birthday, String nationality) {
        this.name = name;
        this.id = id;
        this.birthday = birthday;
        this.nationality = nationality;
    }
}
